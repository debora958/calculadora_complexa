class calculadora_num_complexos:
    def __init__(self, numero_1, numero_2, numero_3):
        self.numero_1 = numero_1 
        self.numero_2 = numero_2
        self.numero_3 = numero_3

    def somar(self):
        resultado = (self.numero_1 + self.numero_2) + self.numero_3
        print(resultado)

    def subtrair(self):
        resultado = (self.numero_1 - self.numero_2) - self.numero_3
        print(resultado)

    def dividir(self):
        resultado = (self.numero_1 / self.numero_2) / self.numero_3

    def multiplicar(self):
        resultado = (self.numero_1 * self.numero_2) * self.numero_3








